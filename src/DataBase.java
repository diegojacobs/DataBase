
public class DataBase {
	
	private String name;
	
	public DataBase()
	{
		this.name = "";
	}

	public DataBase(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	

}
